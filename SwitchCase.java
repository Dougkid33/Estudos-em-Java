package Estudos;

public class SwitchCase {

	public static void main(String[] args) {
		int number = 55;
		switch(number) {
		case 1:
			System.out.println("O número 1");
			break;
		case 3:
			System.out.println("O número 3");
			break;
		case 5:
			System.out.println("O numero e 5");
		default:
			System.out.println("O numero e " + number);
			break;
			
		}

	}

}
